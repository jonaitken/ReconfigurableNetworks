package networkcalculus;

public class EdgeProperties {
	
	private String stringSource;
	private String stringDest;
	
	private int intOriginalEdgeID;
	
	private int intServerID;
	
	public EdgeProperties() {
		
	}
	
	public void setSource(String source) {
		this.stringSource=new String(source);
	}
	
	public String getSource() {
		return this.stringSource;
	}
	
	public void setDestination(String destination) {
		this.stringDest=new String(destination);
	}
	
	public String getDestination() {
		return this.stringDest;
	}
	
	public void setOriginalEdgeID(int intID) {
		this.intOriginalEdgeID=intID;
	}
	
	public int getOriginalEdgeID() {
		return this.intOriginalEdgeID;
	}
	
	public void setServerID(int intServerID) {
		this.intServerID=intServerID;
	}
	
	public int getServerID() {
		return this.intServerID;
	}
}
