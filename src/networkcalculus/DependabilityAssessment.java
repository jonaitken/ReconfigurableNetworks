package networkcalculus;

import java.util.Vector;

public class DependabilityAssessment {
	
	private String sourceRemoved;
	private String destRemoved;
	
	private Vector<ConnectionsInformation> connectionsInformation;
	
	private boolean boolValidNetwork;
	
	public DependabilityAssessment() {
		
	}
	
	public void setConnectionsInformation(Vector<ConnectionsInformation> connectionsInformation) {
		this.connectionsInformation=new Vector<ConnectionsInformation>(connectionsInformation);
	}
	
	public Vector<ConnectionsInformation> getConnectionsInformation() {
		return this.connectionsInformation;
	}
	
	public void setSourceRemoved(String src) {
		this.sourceRemoved=new String(src);
	}
	
	public String getSourceRemoved() {
		return this.sourceRemoved;
	}
	
	public void setDestinationRemoved(String dest) {
		this.destRemoved=new String(dest);
	}
	
	public String getDestinationRemoved() {
		return this.destRemoved;
	}
	
	public void setValidNetwork(boolean boolValidity) {
		this.boolValidNetwork=boolValidity;
	}
	
	public boolean isValidNetwork() {
		return this.boolValidNetwork;
	}
	
}
